def new_doc():
    d = Document()
    st = d.styles["Normal"]
    st.font.name = "Times New Roman"
    st.font.size = Pt(11)
    st.paragraph_format.space_after = Pt(4)
    for s in d.sections:
        s.top_margin = s.bottom_margin = Cm(2)
        s.left_margin = Cm(2.5)
        s.right_margin = Cm(1.5)
    return d


def P(d, text="", bold=False, center=False, justify=True, right=False, size=None):
    p = d.add_paragraph()
    p.alignment = (AL.RIGHT if right else AL.CENTER if center
                   else AL.JUSTIFY if justify else AL.LEFT)
    _add_runs(p, text, bold, size)
    return p


def _add_runs(p, text, bold=False, size=None):
    """Поддержка **жирного** внутри текста через двойные звёздочки."""
    parts = text.split("**")
    for i, seg in enumerate(parts):
        if not seg:
            continue
        r = p.add_run(seg)
        r.bold = bold or (i % 2 == 1)
        if size:
            r.font.size = Pt(size)


def bullet(d, text, bold=False):
    """Пункт-тире (как в офертах: «- ...»)."""
    p = d.add_paragraph()
    p.alignment = AL.JUSTIFY
    p.paragraph_format.left_indent = Cm(0.5)
    _add_runs(p, "— " + text, bold)
    return p


def marker(d, tag):
    p = d.add_paragraph()
    r = p.add_run(tag)
    r.font.size = Pt(8)
    r.font.color.rgb = RGBColor(0xBB, 0xBB, 0xBB)
    return p


def set_widths(table, widths_cm):
    table.autofit = False
    for row in table.rows:
        for i, w in enumerate(widths_cm):
            row.cells[i].width = Cm(w)


def payment_header(d):
    """Шапка-«платёжка» с реквизитами получателя (Исполнителя).
    КПП показывается только для ЮЛ (через тег-условие)."""
    t = d.add_table(rows=3, cols=6)
    t.style = "Table Grid"
    # строка 1: Банк получателя | БИК | значение
    t.cell(0, 0).merge(t.cell(0, 1))
    t.cell(0, 0).paragraphs[0].add_run("{{ исполнитель_банк }}\nБанк получателя")
    t.cell(0, 2).paragraphs[0].add_run("БИК")
    t.cell(0, 3).merge(t.cell(0, 5)).paragraphs[0].add_run("{{ исполнитель_бик }}")
    # строка 2: ИНН | значение | КПП | значение | Сч.№ корр | значение
    t.cell(1, 0).paragraphs[0].add_run("ИНН")
    t.cell(1, 1).paragraphs[0].add_run("{{ исполнитель_инн }}")
    t.cell(1, 2).paragraphs[0].add_run("КПП")
    t.cell(1, 3).paragraphs[0].add_run("{{ исполнитель_кпп }}")
    t.cell(1, 4).paragraphs[0].add_run("Сч. №")
    t.cell(1, 5).paragraphs[0].add_run("{{ исполнитель_корсчет }}")
    # строка 3: Получатель | ... | Сч.№ | значение
    t.cell(2, 0).merge(t.cell(2, 1))
    t.cell(2, 0).paragraphs[0].add_run("{{ исполнитель_фио }}\nПолучатель")
    t.cell(2, 2).paragraphs[0].add_run("Сч. №")
    t.cell(2, 3).merge(t.cell(2, 5)).paragraphs[0].add_run("{{ исполнитель_счет }}")
    return t


def items_table(d):
    """Таблица позиций: № | Наименование | Кол-во | Цена | Сумма + Итого."""
    t = d.add_table(rows=4, cols=5)
    t.style = "Table Grid"
    for i, h in enumerate(["№", "Наименование", "Кол-во, шт.", "Цена, руб.",
                           "Сумма, руб."]):
        t.cell(0, i).paragraphs[0].add_run(h).bold = True
    t.cell(1, 0).paragraphs[0].add_run("{%tr for у in услуги %}")
    for i, v in enumerate(["{{ у.номер }}", "{{ у.наименование }}", "{{ у.колво }}",
                           "{{ у.цена }}", "{{ у.стоимость }}"]):
        t.cell(2, i).paragraphs[0].add_run(v)
    t.cell(3, 0).paragraphs[0].add_run("{%tr endfor %}")
    set_widths(t, [1, 8, 2.5, 3, 3])
    return t


def zakazchik_line(d, label="Заказчик"):
    P(d, f"**{label}: **{{{{ заказчик_наименование }}}}, ИНН {{{{ заказчик_инн }}}}, "
         f"КПП {{{{ заказчик_кпп }}}}, адрес: {{{{ заказчик_адрес }}}}.")


def ispolnitel_line(d, role="Исполнитель"):
    P(d, f"**{role}:** {{{{ исполнитель_фио }}}}, ИНН {{{{ исполнитель_инн }}}}, "
         f"адрес: {{{{ исполнитель_адрес }}}}.")


def itogo_block(d):
    P(d, "**Итого: {{ стоимость_цифрами }} руб.**", justify=False)
    P(d, "**НДС:** {{ ндс_строка }}", justify=False)
    P(d, "Всего к оплате: {{ стоимость_прописью }}.", justify=False)


def sign_mp(d):
    P(d)
    P(d, "________________ / {{ исполнитель_подписант_кратко }} /", justify=False)
    P(d, "м.п.", justify=False)


def save(d, folder, name):
    out = os.path.join(BASE, folder)
    os.makedirs(out, exist_ok=True)
    d.save(os.path.join(out, name))


