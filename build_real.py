def H(d, text):
    """Заголовок раздела (римская нумерация)."""
    p = d.add_paragraph()
    p.alignment = AL.CENTER
    p.add_run(text).bold = True


def num_p(d, num, text):
    """Нумерованный пункт «N. текст»."""
    p = d.add_paragraph()
    p.alignment = AL.JUSTIFY
    _add_runs(p, f"{num}. {text}")


def sub_p(d, text):
    """Подпункт с отступом, без номера (как в оригинале — маркер списка)."""
    p = d.add_paragraph()
    p.alignment = AL.JUSTIFY
    p.paragraph_format.left_indent = Cm(0.6)
    _add_runs(p, text)


