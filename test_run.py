assert "СЗ-1" in nums  # это просто значения из теста выше
print("Журнал хранит введённые номера: ОК")

# архив: меняем ОГРН Ромашки -> старая версия в архив, потом возвращаем
ром = dict(исполнители["ЮЛ"], огрн="1217700000000")
cur = core.find_party(ром)
diff = core.party_diff(cur, ром)
assert [d[0] for d in diff] == ["огрн"], diff
assert core.save_party(ром, user="Никита") == "updated"
hist = core.party_history(ром)
assert len(hist) == 1 and hist[0]["огрн"] == "1157746000000" and hist[0]["кем"] == "Никита"
core.restore_party(hist[0], user="Олег")
assert core.find_party(ром)["огрн"] == "1157746000000"
assert len(core.party_history(ром)) == 2  # откат тоже в архиве
print("Архив и восстановление: ОК")

