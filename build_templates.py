    P(d)
    P(d, isp["преамбула"] + ЗАК_ПРЕАМБУЛА)
    works_table(d, with_qty=False)
    P(d)
    if ндс:
        P(d, "1. Общая стоимость Услуг составляет {{ итого_с_ндс_цифрами }} руб. "
             "({{ итого_с_ндс_прописью }}), в том числе НДС {{ ндс_ставка }}% — "
             "{{ ндс_сумма }} руб.")
    else:
        P(d, "1. Общая стоимость Услуг составляет {{ стоимость_прописью }}.")
    P(d, "2. Срок оказания услуг: до {{ услуги_окончание }}")
    P(d, "3. Заказчик обязуется оплатить Услуги в размере 100% их стоимости до начала "
         "оказания Услуг путем перечисления денежных средств на банковский счет "
         "Исполнителя на основании счета на оплату, выставленного Исполнителем. "
         "Акт оказанных услуг направляется Исполнителем Заказчику по факту оказания "
         "Услуг.")
    P(d, "4. Настоящее приложение составлено в 2 (Двух) экземплярах, имеющих "
         "одинаковую юридическую силу, по одному экземпляру для каждой Стороны.")
    P(d)
    sign_pair(d, "Исполнитель", "{{ исполнитель_фио }}", подпись_исп,
              "{{ заказчик_наименование }}", ["{{ заказчик_реквизиты }}", "",
              "Представитель по доверенности"],
              "_____________ / {{ заказчик_подписант_кратко }} /")
