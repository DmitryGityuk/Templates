    a = t.cell(0, 0)
    a.paragraphs[0].add_run("Заказчик:").bold = True
    a.add_paragraph("{{ заказчик_наименование }}")
    a.add_paragraph()
    a.add_paragraph("_____________________ /{{ заказчик_подписант_кратко }}/")
    b = t.cell(0, 1)
    b.paragraphs[0].add_run("Исполнитель:").bold = True
    b.add_paragraph("{{ исполнитель_фио }}")
    b.add_paragraph()
    b.add_paragraph("_____________________ /{{ исполнитель_фио_кратко }}/")


