import json, os
from docx import Document
from docx.shared import Pt, Cm, RGBColor
from docx.enum.text import WD_ALIGN_PARAGRAPH as AL

BASE = os.path.join(os.path.dirname(os.path.abspath(__file__)), "templates")


def new_doc():
    d = Document()
    st = d.styles["Normal"]
    st.font.name = "Times New Roman"
    st.font.size = Pt(11)
    st.paragraph_format.space_after = Pt(4)
    for s in d.sections:
        s.top_margin = s.bottom_margin = Cm(2)
        s.left_margin = Cm(2.5)
        s.right_margin = Cm(1.5)
    return d


def P(d, text="", bold=False, center=False, justify=True, right=False):
    p = d.add_paragraph()
    p.alignment = (AL.RIGHT if right else AL.CENTER if center
                   else AL.JUSTIFY if justify else AL.LEFT)
    r = p.add_run(text)
    r.bold = bold
    return p


def marker(d, tag):
    p = d.add_paragraph()
    r = p.add_run(tag)
    r.font.size = Pt(8)
    r.font.color.rgb = RGBColor(0xAA, 0xAA, 0xAA)
    return p


def head_table(d, left, right):
    t = d.add_table(rows=1, cols=2)
    t.cell(0, 0).paragraphs[0].add_run(left)
    c = t.cell(0, 1).paragraphs[0]
    c.alignment = AL.RIGHT
    c.add_run(right)


def works_table(d, with_qty=True):
    cols = 5 if with_qty else 2
    t = d.add_table(rows=4, cols=cols)
    t.style = "Table Grid"
    heads = (["№", "Наименование работ", "Кол-во", "Цена, руб.", "Сумма, руб."]
             if with_qty else ["Наименование услуг/работ", "Стоимость, руб."])
    for i, h in enumerate(heads):
        t.cell(0, i).paragraphs[0].add_run(h).bold = True
    t.cell(1, 0).paragraphs[0].add_run("{%tr for у in услуги %}")
    row = (["{{ у.номер }}", "{{ у.наименование }}", "{{ у.колво }}",
            "{{ у.цена }}", "{{ у.стоимость }}"] if with_qty
           else ["{{ у.наименование }}", "{{ у.стоимость }}"])
    for i, v in enumerate(row):
        t.cell(2, i).paragraphs[0].add_run(v)
    t.cell(3, 0).paragraphs[0].add_run("{%tr endfor %}")
    return t


def sign_pair(d, left_title, left_name, left_sig, right_title, right_lines, right_sig):
    t = d.add_table(rows=1, cols=2)
    t.style = "Table Grid"
    a = t.cell(0, 0)
    a.paragraphs[0].add_run(left_title).bold = True
    a.add_paragraph(left_name)
    a.add_paragraph()
    a.add_paragraph(left_sig)
    b = t.cell(0, 1)
    b.paragraphs[0].add_run(right_title).bold = True
    for line in right_lines:
        b.add_paragraph(line)
    b.add_paragraph()
    b.add_paragraph(right_sig)


